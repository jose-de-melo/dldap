#!/bin/bash
####################################################
## Caixa de texto utilizada para exibir mensagens  #
####################################################

###################################################
## Título da janela                               #
###################################################
backtitle=$1

####################################################
## Título da caixa                                 #
####################################################
title=$2

####################################################
## Mensagem a ser exibida na caixa                 #
####################################################
message=$3


#######################################################
## Exibindo a caixa de texto com os valores recebidos #
#######################################################
dialog	\
  --cr-wrap                                            \
  --backtitle "$backtitle"                \
   --title "$title"                             \
   --msgbox "$message"  \
   6 40
